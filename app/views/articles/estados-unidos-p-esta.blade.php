                <li>
                    Rápido - 24 horas: El precio por solicitante es de USD $140.99.
                </li>
                <li>
                    Urgente - 12 horas: El precio por solicitante es de USD $200.99.
                </li>
            </ul>

            Estos precios incluyen la tarifa gubernamental.

            <h2>¿Necesito solicitar el ESTA de Estados Unidos para hacer escala en el país?</h2>
            Sí, también debes solicitar un ESTA si estás haciendo escala en el país, incluso si no sales del área de tránsito en el aeropuerto.

            <h2>Requisitos y restricciones de entrada a Estados Unidos</h2>
