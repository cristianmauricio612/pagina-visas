                <ul>
                    <li>Visa de 30 días con doble entrada: Esta visa te autoriza dos ingresos a India y es válida por 30 días. Es importante ingresar al país dentro de ese plazo. Una vez dentro, puedes permanecer hasta 30 días desde tu primera entrada.
                        </li>
