                <h2>Cómo interpretar la validez de tu visa de turista</h2>
                Validez: ¿Por cuánto tiempo es válida tu visa?

                <ul>
                    <li>Estancia máxima: El tiempo máximo que puedes quedarte en India cada vez que la visites.
                        </li>
                    <li>Fecha de expiración: Depende del tipo de visa que tengas.</li>
                </ul>

                Por ejemplo:

                Si tienes una visa de 30 días y la fecha de expiración es el 14 de marzo:

                La última fecha en que puedes entrar a India es el 14 de marzo de 2024.

                Esta es la fecha de expiración de tu visa. A partir de esta fecha, tienes permitido quedarte en el país hasta por 30 días.

                Si tienes una visa de 1 año o 5 años y la fecha de expiración es el 14 de marzo:

                La última fecha en que puedes salir de India es el 14 de marzo de 2024.

                En este caso, la fecha de expiración es la fecha en la que debes salir de India.

                Puedes quedarte en India durante el tiempo máximo permitido en tu visa, pero asegúrate de no exceder el período de validez de la misma.

                <h2>¿Puedo extender la duración de mi visa?</h2>
